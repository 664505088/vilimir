- (NSString *)MD5Valude {
    if(self == nil || [self length] == 0)
        return nil;
    
    const char *value = [[self uppercaseString] UTF8String];
    
    unsigned char result[CC_MD5_DIGEST_LENGTH];
    CC_MD5(value, (unsigned)strlen(value), result);
    NSMutableString *res = [[NSMutableString alloc] initWithCapacity:CC_MD5_DIGEST_LENGTH];
    for(NSInteger count = 0; count < CC_MD5_DIGEST_LENGTH; count++){
        [res appendFormat:@"%02x",result[count]];
    }
    return res;
}


